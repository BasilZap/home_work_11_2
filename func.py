def set_text_upper(some_text):
    """
    Функция делает заглавными все
    буквы во всех словах
    """
    return str(some_text).upper()


def set_text_title(some_text1):
    """
    Функция делает заглавными все
    первые буквы каждого слова
    :param some_text: str
    :return: str
    """
    return str(some_text1).title()
